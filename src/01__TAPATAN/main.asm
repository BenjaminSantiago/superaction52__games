;includes
;---------------------------------------------------------------
;header for this ROM
.INCLUDE "inc/header.inc"

;code to start up SNES (clear registers)
;and macros for graphics
.INCLUDE "inc/init.inc"
.INCLUDE "inc/load_graphics.asm"

;variables
;---------------------------------------------------------------'
.EQU    gameMODE    $0221

; game modes
;-----------
; 00 --> title
; 01 --> options? 
; 02 --> game -> drop phase
; 03 --> game -> move phase
; 04 --> game -> game over

.EQU    BOARD       $0222 ; -> 0224
.EQU    BOARD_01    $0223
.EQU    BOARD_02    $0224

; The BOARD 
;----------
; it has 9 locations, 3 rows of 3. 
; each location is represented by 2 bits 
; empty         -> 00
; p1 occupied   -> 01
; p2 occupied   -> 10

; memory locations
; $0222         - $0223         - $0224
; 00 00 00 | 00 - 00 00 | 00 00 - 00

; GAME BOARD ROWS
;   TOP    | MIDDLE     | BOTTOM
; 00 00 00 | 00 - 00 00 | 00 00 - 00

; how long to wait for sprite frames
.EQU    sprite__wait    $0225

; these should be for each coin
; counter for distance between sprites
.EQU    sprite__00__P1__counter $0226
; which sprite we show
.EQU    sprite__00__P1__sprite  $0227

; location
.EQU    sprite__00__P1__x       $0228
.EQU    sprite__00__P1__y       $0229

.EQU sprite__00__P2__counter    $022A
.EQU sprite__00__P2__sprite     $022B
.EQU sprite__00__P2__x          $022C
.EQU sprite__00__P2__y          $022D

;variables for controller
.EQU joy1H__c   $022E
.EQU joy1H__p   $022F
.EQU joy1H__h   $0230

.EQU joy1L__c   $0231
.EQU joy1L__p   $0232
.EQU joy1L__h   $0233

.EQU HDMA_table  $0234

.EQU PILL__player__offset  $023B
.EQU PILL__player__direction $023D

.EQU CURSOR__P1__x  $023E
.EQU CURSOR__P1__y  $023F

; M A I N  C O D E
; where the processor goes on reset
;---------------------------------------------------------------
.BANK 0 SLOT 0
.ORG 0
.SECTION "MainCode"

Start:
    ;start up the SNES
    InitSNES   

    ;"initialize" the "variables"
    ;------------------------------------------------------
    stz gameMODE
    stz BOARD
    stz sprite__00__P1__counter    
    stz sprite__00__P1__sprite

    stz sprite__00__P2__counter    
    stz sprite__00__P2__sprite

    stz PILL__player__offset
    stz PILL__player__direction

    lda #$06
    sta sprite__wait

    lda #$50
    sta CURSOR__P1__x
    
    lda #$20
    sta CURSOR__P1__y
    ;------------------------------------------------------

    ; LOAD GRAPHICS
    ;------------------------------------------------------
    LoadPalette bg__palette,        0,      16 
    LoadPalette sprites__palette,   128,    16
    LoadBlockToVRAM bg__tiles,      $0000,  $1800
    LoadBlockToVRAM sprites__tiles, $2000,  $3800
    ;------------------------------------------------------
        
    ; TILE MAPS
    ;------------------------------------------------------
    ; increment after writing to $2119
    lda #$80
    sta $2115

    ; tilemap offset --> $1000
    ; tilemap size   --> 1 screen in either direction
    lda #$10
    sta $2107
    
    ; tilemap offset --> $1800
    ; tilemap size   --> 1 screen in either direction
    lda #$18
    sta $2108 
    
    ; DMA the tile maps
    LoadBlockToVRAM tilemap__BG02__drop, $1000, 1024 
    LoadBlockToVRAM tilemap__BG01, $1800, 1024

    ; SPRITES
    ;------------------------------------------------------
    jsr SpriteInit

    ;16x16 and 32x32 sprites 
    ;address offset
    lda #%01100001          
    sta $2101
    ;------------------------------------------------------

    ; START IT UP!
    ;------------------------------------------------------
    jsr SetupVideo
    
    ; we want BG 1 & BG 2 to be 16 x 16
    ; we want BG mode 1
    lda #%00110001
    sta $2105
   
    ; BG1 & BG2 character VRAM offset
    stz $210B

    ldx #$0100
    stx $2110

    ; enable BG 1 & 2
    ; enable sprites
    lda #%00010011
    sta $212C
    
    ; enable interrupts
    lda #$81
    sta $4200    

    ;enable 9th x-bits / Embiggen sprites
    lda #%00001010
    sta $0200

    jsr MAKE__HDMAtable
    jsr InitHDMA

    ; turn on screen full brightness
    lda #$0F
    sta $2100
    ;------------------------------------------------------


;M A I N  L O O P! 
;---------------------------------------------------------------
forever:
    wai;t for an interrupt y'all!
    ;TITLE
    ;----------------------------------------------------------
    ; title game code 
    ; --> display title
    ;----------------------------------------------------------


    ;GAME 
    ;----------------------------------------------------------
    
    ;set x (location 1) 
    lda #$50
    sta $0004
    
    ;set y (screen.height * .5/height of sprite)
    lda #$20
    sta $0005
    
    ;first tile
    ldx sprite__00__P1__sprite
    lda.l sprite__O__indices, X
    sta $0006
    
    lda #%00100000
    sta $0007

    ;enable 9th x-bits / Embiggen sprites
    lda #%00101010
    sta $0200

    ;animate sprite
    lda sprite__00__P1__sprite
    cmp #$0D
    bne + 

    bra done_with_anim
+
    lda sprite__00__P1__counter
    cmp sprite__wait
    bne +

    stz sprite__00__P1__counter
    inc sprite__00__P1__sprite


    bne + 


+   inc sprite__00__P1__counter

 done_with_anim:
 ;sprite 2
    ;---------------------------------
    ;set x (location 1) 
    lda #$90
    sta $0008
    
    ;set y (screen.height * .5/height of sprite)
    lda #$20
    sta $0009
    
    ;first tile
    ldx sprite__00__P2__sprite
    lda.l sprite__X__indices, X
    sta $000A
    
    ;set tile priority 
    ;high bit of tile number (0th)
    lda #%00100001
    sta $000B

    ;animate sprite
    lda sprite__00__P2__sprite
    cmp #$0B
    bne + 

    bra done_with_anim__2
+
    lda sprite__00__P1__counter
    cmp sprite__wait
    bne +

    stz sprite__00__P2__counter
    inc sprite__00__P2__sprite


    bne + 


+   inc sprite__00__P2__counter

 done_with_anim__2:
    ; check direction 
    ; 0 is left, 1 is right
    lda PILL__player__direction
    cmp #$00
    bne move_right

    ; if 0 
    ; check if we are at the max #$30    
    rep #$20
    lda PILL__player__offset
    cmp #$0040 
    bcc +
    sep #$20

    ; if we are, change direction
    lda #$01 
    sta PILL__player__direction
    bra done_with_PILL

+   
    ; move to the left (adc)
    rep #$20
    lda PILL__player__offset
    clc 
    adc #$0002
    sta PILL__player__offset
    sta HDMA_table+1
    sep #$20
    bra done_with_PILL

 move_right:
    ; check if we are at #$00
    rep #$20
    lda PILL__player__offset
    cmp #$0002 
    bcs +
    sep #$20
    
    ; if 1
    ; if we are, change direction
    lda #$00
    sta PILL__player__direction
    bra done_with_PILL

    ; move to the right (sbc)
+   rep #$20
    lda PILL__player__offset
    sec 
    sbc #$0002
    sta PILL__player__offset
    sta HDMA_table+1
    sep #$20
    
 done_with_PILL:
    ;---------------------------------
    ;set x (location 1)
    lda CURSOR__P1__x
    
    sta $0000
    
    ;$50, $90, $D0
    ;$20, $60, $A0
    ;set y (screen.height * .5/height of sprite)
    lda CURSOR__P1__y
    ; something might be up with the sprite?
    dec a
    sta $0001
    
    ;first tile
    lda #$C8
    sta $0002
    
    ;set tile priority 
    ;high bit of tile number (0th)
    lda #%00110000
    sta $0003
    ;---------------------------------

 check_controls:
    
 check__P1__UP:
    lda joy1H__p
    and #%00001000
    beq check__P1__DOWN

    ;---------------------------
    lda CURSOR__P1__y
    cmp #$20
    beq check__P1__DOWN

    lda CURSOR__P1__y
    sec
    sbc #$40
    sta CURSOR__P1__y
    ;---------------------------

 check__P1__DOWN:
    lda joy1H__p
    and #%00000100
    beq check__P1__LEFT

    ;---------------------------
    lda CURSOR__P1__y
    cmp #$A0
    beq check__P1__LEFT

    lda CURSOR__P1__y
    clc
    adc #$40
    sta CURSOR__P1__y
    ;---------------------------

 check__P1__LEFT:
    lda joy1H__p
    and #%00000010
    beq check__P1__RIGHT

    ;---------------------------
    lda CURSOR__P1__x
    cmp #$50
    beq check__P1__RIGHT

    lda CURSOR__P1__x
    sec
    sbc #$40
    sta CURSOR__P1__x
    ;---------------------------

 check__P1__RIGHT:
    lda joy1H__p
    and #%00000001
    beq done_with_P1CONTROL
    
    ;---------------------------
    lda CURSOR__P1__x
    cmp #$D0
    beq done_with_P1CONTROL

    lda CURSOR__P1__x
    clc
    adc #$40
    sta CURSOR__P1__x
    ;---------------------------
    
 done_with_P1CONTROL:
    ; --> drop phase
    ; ----> start player 1
    
    ; ----> current player drops
    
    ; --------> selection sound as they move locations
    ; --------> make sure location is legal
    ; --------> change color/make a bad ding if they try to drop
    ; --------> nice ding / animation if it works
    ; --------> update connector graphics
     
    ; --------> check win conditions 
    ; (only do this at the very end)

    ; ----> switch to other player (back up to current player drops)

    ; --> move phase

    ; ----> current player moves

    ; --------> selection sound as they move locations
    ; --------> player selects one of their things
    ; --------> player selects legal location to move to
    ; --------> process move

    ; --------> check win conditions

    ; --------> switch players
    
    ; --> game over 
    ; ----> who won?
    ; ----> show that player won 

    ; --> reset stuff
    ; --> go back to title    
    ;----------------------------------------------------------

    jmp forever    ;<-- we outttttt t t t t t t t
;---------------------------------------------------------------


;S U B R O U T I N E S

;NMI (vblank) code
;---------------------------------------------------------------
VBlank:
	pha
	phx
	phy    
    ;-----------------

    rep #$10    
    sep #$20
    
    ; "shadow OAM" --> actual OAM
    ;---------------------------------
    stz $4300

    lda #$04
    sta $4301

    lda #$00
    sta $4302
    sta $4303

    lda #$7E
    sta $4304

    lda #$20
    sta $4305
    lda #$02
    sta $4306

    lda #$01
    sta $420B
    ;---------------------------------

    ; CONTROLLER
    ;-------------------------------------
    ; get joypad status
    ; wait until it is ready
-
    lda $4212
    and #$01
    bne -
    
    ;read controller 1 high
    ;store current in Y (for p)
    ldy joy1H__c

    ;get current
    lda $4219
    sta joy1H__c

    ;switch
    tya 
    
    ;figure out new presses (p)
    ;figure out what was held (h)
    eor joy1H__c
    and joy1H__c
    sta joy1H__p
    tya 
    and joy1H__c
    sta joy1H__h

    ;read controller 1 high
    ;store current in Y (for p)
    ldy joy1L__c

    ;get current
    lda $4218
    sta joy1L__c

    ;switch
    tya 
    
    ;figure out new presses (p)
    ;figure out what was held (h)
    eor joy1L__c
    and joy1L__c
    sta joy1L__p
    tya 
    and joy1L__c
    sta joy1L__h

    ;HDMA stuff
    stz $210D
    stz $210D

    lda #$02
    sta $420C

    lda $4210
    ;-----------------
	ply
    plx
    pla 
	
    sep #$20
    rti

;---------------------------------------------------------------

;initialize the sprites to be off-screen
;(this is only in RAM, still has to be 
;transferred to the OAM)
;---------------------------------------------------------------
SpriteInit:
	php	

	rep	#$30	;16bit mem/A, 16 bit X/Y
	
	ldx #$0000
    lda #$F001
_setoffscr:
    sta $0000,X
    inx
    inx
    inx
    inx
    cpx #$0200
    bne _setoffscr
;-------------------
	lda #$5555
_clr:
	sta $0000, X		;initialize all sprites to be off the screen
	inx
    inx
	cpx #$0220
	bne _clr
;-------------------

	plp
	rts
;---------------------------------------------------------------

;set up the "general video"-type registers
;---------------------------------------------------------------
SetupVideo:
    php
    
    ;set XY/A
    rep #$10
    sep #$20
    
    stz $2102
    stz $2103
    
    ;transfer sprite data into OAM
    ;----------------------------------

    ; DMA params
    LDA #$00
    STA $4300      ; DMAP

    LDA #$04
    STA $4301      ; BBAD = $2104

    LDA #$00
    STA $4302
    STA $4303      ; source offset

    LDA #$7E
    STA $4304      ; source bank

    LDA #$20
    STA $4305
    LDA #$02
    STA $4306      ; $0220 bytes

    LDA #$01
    STA $420B      ; start DMA
    ;----------------------------------

    plp
    rts
;---------------------------------------------------------------

;---------------------------------------------------------------
InitHDMA:
    lda #$02 ; mode 2 = 2 bytes per scanline
    sta $4310 ; DMAP1


    lda #$0D ; $210D = BG1 HOFS
    sta $4311 ; BBAD1


    lda #<HDMA_table
    sta $4312 ; table low
    lda #>HDMA_table
    sta $4313 ; table high


    lda #:HDMA_table
    sta $4314 ; table bank

    rts
;---------------------------------------------------------------

;---------------------------------------------------------------
MAKE__HDMAtable:
    lda #32
    sta HDMA_table

    stz HDMA_table + 1
    stz HDMA_table + 2

    lda #192
    sta HDMA_table + 3

    stz HDMA_table + 4
    stz HDMA_table + 5

    stz HDMA_table + 6
    rts
;---------------------------------------------------------------
; (END of SUBROUTINES)
.ENDS

